    {
        //Add order number
        $order->number = str_pad($order->id, 8, "0", STR_PAD_LEFT).'-'.date('Y');
        $order->update();
        Notification::send(
            User::permission(['ordenes'])->get(),
            new OrderNew($order)
        );
    }

